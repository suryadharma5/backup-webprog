    {
        // dd($request);
        $validatedData = $request->validate([
            'reply_text' => 'required|max:700',
            'menfess_id' => 'required'
        ]);

        $validatedData['user_id'] = auth()->user()->id;

        MenfessReply::create($validatedData);

        return redirect('/menfess')->with('success', 'Jawaban anda berhasil diunggah');
    }

